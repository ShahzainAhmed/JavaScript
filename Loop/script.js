var count = 0;

// While loop 
while (count < 10) {
    document.write(count + " " + "While Loop by Shahzain Ahmed", "<br>");
    count++; // count = count + 1
}

document.write("<br>"); // line break

// Do while loop
do {
    document.write(count + " " + "Do While Loop by Shahzain Ahmed", "<br>");
    count++;
} while (count < 15);

document.write("<br>"); // line break

// for loop
for (var newCount = 5; newCount < 10; newCount++) {
    document.write(newCount + " " + "For Loop by Shahzain Ahmed", "<br>");
}

